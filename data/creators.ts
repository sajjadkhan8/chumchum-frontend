const generateContentPreviews = (creatorName: string): ContentPreview[] => {
  const previews: ContentPreview[] = [];
  for (let i = 1; i <= 6; i++) {
    previews.push({
      id: `content-${creatorName}-${i}`,
      type: i % 3 === 0 ? 'video' : 'image',
      thumbnail: `https://picsum.photos/seed/${creatorName}${i}/400/400`,
      url: `https://picsum.photos/seed/${creatorName}${i}/800/800`,
      platform: ['instagram', 'tiktok', 'youtube'][i % 3] as Platform,
      views: Math.floor(Math.random() * 100000) + 10000,
      likes: Math.floor(Math.random() * 10000) + 1000,
    });
  }
  return previews;
};

